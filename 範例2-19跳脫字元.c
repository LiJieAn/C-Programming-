#include<stdio.h>

int main()
{
	char c1='\'';/*將單引號設定給c1*/
	char c2='\"';/*將雙引號設定給c2*/
	printf("c1=[%c] \n",c1);
	printf("c2=[%c] \n ",c2);
	printf("[\'\"] \n");/*將\' \"合併做輸出*/ 

	return 0;
	 
}


/*'單引號' "雙引號" 倒斜線\
這些特殊意義的字元無法如一般字元直接被輸出→透過跳脫字元處理 
\'→單引號'
\"→雙引號"
\\→倒斜線\
*/ 
/*倒斜線作為跳脫字元的識別淑號→沒有跟著一起輸出*/ 
