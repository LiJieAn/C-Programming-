#include<stdio.h>

int main()
{
	printf("%*d \n",5,100);/*以五個字元寬度輸出 長度不及5的數值 以空白補齊 printf()預設將數字靠右對齊*/ 
	printf("%*d \n",5,2012);
	printf("%*d \n",5,1);
	printf("%*d \n",5,12345);
	return 0;
	
 } 
 /*
 *→在輸出時可預先設定寬度的符號
 printf("%*d",5,1)→以五個字元寬度輸出1 
 在程式碼中間嵌入 * 就可在第一個引數前指定所要預留的寬度
 */
