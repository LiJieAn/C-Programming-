#include<stdio.h>

int main()
{
	char  c1='A';
	char  c2='a';
	printf("A的字元編碼=%d \n",c1);
	printf("a的字元編碼=%d \n",c2);
	return 0;
	
 } 

//A->65 Z->90 a->97


/*可指定%d來輸出特定字元所對應的編碼值
ex:
char x ='A';
printf("%d",x);
→得到65的輸出結果*/ 
/*%c：以字元方式輸出
%d：10 進位整數輸出*/ 
