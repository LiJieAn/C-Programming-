/*算數運算子 運算過程要注意
因運算結果值超出指定型態範圍大小的錯誤→造成溢位*/

#include<stdio.h>

int main()
{
	int a=2147483647;/*→為int型態的最大值*/ 
	int b=2;
	int result =a+b;/*超出int型態所允許的數值範圍*/ 
	printf("a+b等於 %d \n",result);/*顯示結果 a+b等於 -2147483647*/ 
	return 0;
 } 
 /*當將一個超過型態範圍的數值設定給宣告為此型態的變數 此變數會產生[溢位overflow]
  溢位將導致數值回頭從最小的值開始運算*/ 
